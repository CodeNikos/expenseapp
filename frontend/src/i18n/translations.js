export function normalizeLang(value) {
  return value === 'en' ? 'en' : 'es';
}

const GUEST_LANG_KEY = 'expenseapp_guest_lang';

export function getStoredGuestLang() {
  if (typeof localStorage === 'undefined') {
    return 'es';
  }
  return normalizeLang(localStorage.getItem(GUEST_LANG_KEY));
}

export function setStoredGuestLang(lang) {
  const n = normalizeLang(lang);
  localStorage.setItem(GUEST_LANG_KEY, n);
  return n;
}

export function tString(dict, key, vars) {
  const val = key.split('.').reduce((o, k) => (o && o[k] !== undefined ? o[k] : undefined), dict);
  if (typeof val !== 'string') {
    return key;
  }
  if (!vars) {
    return val;
  }
  return val.replace(/\{\{(\w+)\}\}/g, (_, name) => (vars[name] !== undefined ? String(vars[name]) : ''));
}
